  let _lang = localStorage.getItem('poh-miner-lang') || 'en';

  function t(key) {
    return (T[_lang] && T[_lang][key]) || (T.en && T.en[key]) || key;
  }

  function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      el.textContent = t(el.getAttribute('data-i18n'));
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
      el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
    });
  }

  function setLanguage(lang) {
    if (!T[lang]) return;
    _lang = lang;
    localStorage.setItem('poh-miner-lang', lang);
    document.documentElement.lang = lang;
    applyTranslations();
    var sel = document.getElementById('settings-language');
    if (sel) sel.value = lang;
  }

  function buildLangSelector(selectEl) {
    if (!selectEl) return;
    selectEl.innerHTML = '';
    Object.keys(T).forEach(function (code) {
      var opt = document.createElement('option');
      opt.value = code;
      opt.textContent = LANG_NAMES[code] || code;
      if (code === _lang) opt.selected = true;
      selectEl.appendChild(opt);
    });
    selectEl.addEventListener('change', function () { setLanguage(this.value); });
  }

  window.t = t;
  window.setLanguage = setLanguage;
  window.applyTranslations = applyTranslations;
  window.buildLangSelector = buildLangSelector;
  window.LANG_NAMES = LANG_NAMES;

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', applyTranslations);
  } else {
    applyTranslations();
  }
